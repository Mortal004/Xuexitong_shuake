def red(some_str):
    return '\033[31m'+ some_str +'\033[0m'

def yellow(some_str):
    return '\033[31m' + some_str +'\033[0m'

def blue(some_str):
    return '\033[35m' + some_str +'\033[0m'

def green(some_str):
    return '\033[32m' + some_str +'\033[0m'

def magenta(some_str):
    return '\033[35m'+some_str+'\033[0m'

def black(some_str):
    return '\033[30m' + some_str +'\033[0m'
